# @lc code=start
# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution(object):
    def reverseList(self, head):
        """
        :type head: Optional[ListNode]
        :rtype: Optional[ListNode]
        """
        slow, fast = None, head
        while fast:
            temp = fast.next
            fast.next = slow
            slow = fast
            fast = temp

        return slow
        
# @lc code=end

